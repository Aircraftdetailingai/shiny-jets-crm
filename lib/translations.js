/**
 * Get pricing translation. Falls back to English.
 */
export function tp(lang, key) {
  return pricingTranslations[lang]?.[key] || pricingTranslations.en[key] || key;
}

export const SUPPORTED_LANGUAGES = [
  { code: 'en', label: 'English' },
  { code: 'es', label: 'Español' },
  { code: 'fr', label: 'Français' },
  { code: 'pt', label: 'Português' },
  { code: 'de', label: 'Deutsch' },
];

/**
 * Get translation for a key, with {placeholder} replacement.
 * Falls back to English if language or key not found.
 */
export function t(lang, key, replacements = {}) {
  const str = translations[lang]?.[key] || translations.en[key] || key;
  return Object.entries(replacements).reduce(
    (result, [k, v]) => result.replace(new RegExp(`\\{${k}\\}`, 'g'), v),
    str
  );
}

/**
 * Detect language from browser navigator.languages.
 * Returns supported language code or 'en'.
 */
export function detectBrowserLanguage() {
  if (typeof navigator === 'undefined') return 'en';
  const supported = SUPPORTED_LANGUAGES.map(l => l.code);
  for (const lang of (navigator.languages || [navigator.language])) {
    const code = lang?.split('-')[0]?.toLowerCase();
    if (supported.includes(code)) return code;
  }
  return 'en';
}
